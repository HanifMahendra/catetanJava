public class forLoop {
    public static void main(String[] args) {
        // for loop = execute some code a CERTAIN amount of times
        for (int i = 0; i < 10 ; i++) {
            System.out.println(i);
        }

        System.out.println("");

        for (int i = 10; i > 0 ; i--) {
            System.out.println(i);
        }

        System.out.println("");

        for (int i = 0; i <= 10 ; i+=2) {
            System.out.println(i);
        }

        System.out.println("");

        for (int i = 9; i >= 0 ; i-=2) {
            System.out.println(i);
        }
    }
}
